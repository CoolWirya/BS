        public void GetReport(int ZoneCode = 0, int LineNumber = 0, int BusNumebr = 0, DateTime? StartEventDate = null, DateTime? EndEventDate = null, int MinStopTime = 0)
        {
            WebControllers.MainControls.Grid.JGridView jGridView = RadGridReport;
            jGridView.ClassName = "WebBusManagement_FormsReports_JBusTimeStopsAtTheStations_" + ZoneCode.ToString() + "_" + LineNumber.ToString() + "_" + BusNumebr.ToString() + "_" + (StartEventDate != null ? StartEventDate.Value.ToShortDateString() : "") + "_" + (EndEventDate != null ? EndEventDate.Value.ToShortDateString() : "");

            string WhereStr="";
            string WhereStrMinStopTime = "";
            if (StartEventDate!=null && EndEventDate!=null)
                WhereStr += " and bs.Date between '" + ((DateTime)StartEventDate).ToString("yyyy-MM-dd") + "' and '" +  ((DateTime)EndEventDate).ToString("yyyy-MM-dd") + "'";
            if (ZoneCode > 0)
                WhereStr += " and a.ZoneCode = " + ZoneCode;
            if (LineNumber > 0)
                WhereStr += " and a.Code = " + LineNumber;
            if (BusNumebr > 0)
                WhereStr += " and b.Code = " + BusNumebr;
            if (MinStopTime > 0)
                WhereStrMinStopTime += " and SumKam >= " + RadMinStop.Text;
            jGridView.SQL =
@"
    select b.BUSNumber,b.LineNumber LineNumber,(select (select name from autZone where code=zoneCode) from AUTLine where LineNumber=b.LineNumber) ZoneName,b.date,StartTime,FinishTime,ServiceCount,cast(SumKam/60 as nvarchar)+':'+cast(SumKam % 60 as nvarchar) StopTime 
    ,cast(((SumKam/60.0) / ServiceCount) as decimal(2)) Avrage
	from
	(
		select Code,BUSNumber,LineNumber,cast(FirstStationDate as date) date ,Count(*) ServiceCount ,sum(DATEDIFF(minute,LastStationDate , StartTimeNextService)) SumKam 
		,min(LastStationDate)StartTime,max(StartTimeNextService)FinishTime
		from
		(
			select bs.*,LEAD(FirstStationDate,1,FirstStationDate) OVER (PARTITION BY bs.BusNumber,cast(FirstStationDate as date) order by bs.BusNumber,FirstStationDate) StartTimeNextService from AutBusServices bs
            inner join autline a on bs.LineNumber = a.LineNumber
            inner join autbus b on b.busNumber=bs.busnumber
			where isOk in(2,5,11) and Deleted=0 " + WhereStr + @"
		) as a
		group by BUSNumber,cast(FirstStationDate as date),LineNumber,Code
	) b 
	where 1=1 " + WhereStrMinStopTime;
            jGridView.SQLType = (int)WebControllers.MainControls.Grid.SQLTypeEnum.Query;
            jGridView.PageSize = 50;
            jGridView.HiddenColumns = "Code";
            jGridView.Title = "JBusTransactionLowerReportControl";
            jGridView.Buttons = "excel,print,record_print";
            jGridView.PageOrderByField = " BusNumber ";
            jGridView.SumFields = new Dictionary<string, double>();
            jGridView.SumFields.Add("StopTime", 0);

            jGridView.Bind();
        }

